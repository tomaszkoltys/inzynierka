CREATE TABLE role (
    id INT PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE status (
    id INT PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE users (
    id INT PRIMARY KEY AUTO_INCREMENT,
    name VARCHAR(50),
    surname VARCHAR(50),
    username VARCHAR(50),
    password VARCHAR(50),
    email_address VARCHAR(50),
    role INT,
    identity_number VARCHAR(50),
    status INT,
    accepted TINYINT,
    FOREIGN KEY (role) REFERENCES role(id),
    FOREIGN KEY (status) REFERENCES status(id)
);

CREATE TABLE voivodeship (
    id INT PRIMARY KEY,
    name VARCHAR(50) CHARACTER SET `binary`
);

CREATE TABLE county (
    id INT PRIMARY KEY,
    name VARCHAR(50) CHARACTER SET `binary`,
    voivodeship INT,
    FOREIGN KEY (voivodeship) REFERENCES voivodeship(id)
);

CREATE TABLE help_status (
    id INT PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE help_type (
    id INT PRIMARY KEY,
    name VARCHAR(50)
);

CREATE TABLE help (
    id INT PRIMARY KEY,
    author INT,
    supporter INT,
    county INT,
    type INT,
    description VARCHAR(255),
    photo VARCHAR(255),
    side VARCHAR(50),
    help_status INT,
    FOREIGN KEY (author) REFERENCES users(id),
    FOREIGN KEY (supporter) REFERENCES users(id),
    FOREIGN KEY (county) REFERENCES county(id),
    FOREIGN KEY (type) REFERENCES help_type(id),
    FOREIGN KEY (help_status) REFERENCES help_status(id)
);



